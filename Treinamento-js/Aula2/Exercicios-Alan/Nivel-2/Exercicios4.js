//Dada a temperatura, classificá-la como fria (< 15), quente (> 30) e normal (caso contrário);

temperatura = 35

if (temperatura < 15) {
	console.log("A temperatura esta fria")
} else if (temperatura > 30) {
	console.log("A temperatura esta quente")
} else {
	console.log("A temperatura esta normal")
}